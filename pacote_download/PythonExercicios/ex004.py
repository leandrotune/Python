n1 = input('digite algo:')
print('qual é o tipo primitivo ?', type(n1))
print('só tem espaços ?', n1.isspace())
print('só tem numeros ?', n1.isnumeric())
print('e alfabetico ?', n1.isalpha())
print('é alfanumerico', n1.isalnum())
print('está em maiusculas ?', n1.isupper())
print('está em minusculas ?', n1.islower())


















