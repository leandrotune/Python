altura = float(input('Qual é a altura da parede: '))
comprimento = float(input('Qual é o comprimento da parede: '))
area = altura * comprimento
print('Sua parede tem a dimensâo de {}x{} e você precisa de {}m²'.format(altura, comprimento, area))
print('Sua parede tem {}m² e você precisará de {} litros de tintar para pintar essa parede'.format(area, (area/2)))

